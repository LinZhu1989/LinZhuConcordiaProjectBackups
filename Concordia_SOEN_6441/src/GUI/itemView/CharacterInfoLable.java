import java.awt.Point;

import utility.TextZone;
import utility.Zone;

/**
 * this is the label which is used to show the 
 * information of the character.
 */
public class CharacterInfoLable extends TextZone {

	private String m_Label;//the title of the label.
	private String m_Value;//the value of the information.
	
	public CharacterInfoLable(Point pt, int w, int h, Zone parent,
			String label) {
		super(pt, w, h, parent, "");
		this.setTextHeight(20);
		this.setLabel(label);
		this.setValue("");

		this.setJustify(Justify.LEFT);
	}
	
	private String composeString() {
		return String.format("%s:%s", this.m_Label,this.m_Value);
	}

	public String getLabel() {
		return m_Label;
	}

	public void setLabel(String mLabel) {
		this.m_Label = mLabel;
	}

	String getValue() {
		return m_Value;
	}

	public void setValue(String mValue) {
		this.m_Value = mValue;		
		this.setText(composeString());
	}
	
	public int getBorderWidth() {
		return 0;
	}

}
